# * List operations

#%%
l1 = ['apple','banana', 'cherry']
l1.append('orange')
print(l1)

# %%
l1 = ['apple','banana', 'cherry']
l1.insert(0, 'orange')
print(l1)

# %%
l1 = ['apple','banana', 'cherry']
l1.remove('banana')
print(l1)

# %%
l1 = ['apple','banana', 'cherry']
l1.pop()
print(l1)

# %%
l1 = ['apple','banana', 'cherry']
del l1[0]
print(l1)

# %%
l1 = ['apple','banana', 'cherry']
l1.clear()
print(l1)

